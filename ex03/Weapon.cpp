Weapon::Weapon(void) {}

Weapon::Weapon(std::string const type) :
	_type(type) {}

// ************************************************************************* //
//                                Destructors                                //
// ************************************************************************* //

Weapon::~Weapon(void) {}

// ************************************************************************* //
//                                 Accessors                                 //
// ************************************************************************* //

void	Weapon::setType(std::string const type)
{
	this->_type = type;
}

std::string	Weapon::getType(void) const
{
	return this->_type;
}
